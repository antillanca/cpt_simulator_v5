    async def generate_lua(self, module_name: str, description: str = None, retries: int = 3) -> Optional[str]:
        """Generate Lua code for a module or ad-hoc description."""
        module = self.get_module(module_name)
        
        # Use provided description if module not found
        final_desc = description or (module["description"] if module else None)
        if not final_desc:
            return None

        # Determine if this is a physics module (needs inline code vs function)
        physics_keywords = ["move", "velocity", "acceleration", "force", "bounce",
                           "gravity", "friction", "energy", "wave", "reflect",
                           "ohm", "electric", "magnetic", "field", "quantum", "lagrangian", "mechanics", "slit"]
        is_physics = False
        if module and module.get("subject") in ["physics", "advanced"]:
            is_physics = True
        else:
            is_physics = any(kw in (module_name + final_desc).lower() for kw in physics_keywords)

        # Use Tutor to generate the prompt if advanced motor is enabled
        if USE_ADVANCED_MOTOR:
            prompt = await tutor_engine.get_student_prompt(module_name, {})
        else:
            prompt = self.build_prompt(module_name, final_desc, is_physics)

        for attempt in range(retries):
            logger.info(f"[Student] Generating '{module_name}' (attempt {attempt + 1}/{retries}) [Advanced={USE_ADVANCED_MOTOR}]...")
            
            # Select model dynamically (MoE)
            model = await get_active_model(module.get("subject") if module else None)
            
            if USE_ADVANCED_MOTOR:
                # Use the powerful cascading engine (OpenRouter/Nvidia)
                self._last_model_used = "OpenRouter/Tutor"
                response = await tutor_engine.generate_rule(prompt, {})
            else:
                # Use the selected local expert
                self._last_model_used = model
                response = await ollama_generate(prompt, model=model)

            if response:
                # Extract Lua code from response
                lua_code = self._extract_lua(response)
                if lua_code:
                    return lua_code

            await asyncio.sleep(2)

        return None

    def _extract_lua(self, text: str) -> Optional[str]:
        """Extract clean Lua code from model response.
        Handles markdown code blocks, backticks, and extra text.
        """
        import re

        text = text.strip()

        # Try to extract from markdown code block
        # Match ```lua ... ``` or ``` ... ```
        block_match = re.search(r'```(?:lua)?\s*\n(.*?)```', text, re.DOTALL)
        if block_match:
            return block_match.group(1).strip()

        # Try inline code: `code`
        inline_match = re.search(r'`([^`]+)`', text)
        if inline_match:
            candidate = inline_match.group(1).strip()
            if any(kw in candidate.lower() for kw in ["function", "local", "return"]):
                return candidate

        # Remove any remaining backtick lines
        lines = text.split("\n")
        clean_lines = []
        in_block = False
        for line in lines:
            stripped = line.strip()
            if stripped.startswith("```"):
                in_block = not in_block
                continue
            if not stripped.startswith("`"):
                clean_lines.append(line)

        text = "\n".join(clean_lines).strip()

        # Basic validation
        if len(text) < 5 or len(text) > 2000:
            return None

        # Must contain some Lua keywords
        lua_keywords = ["function", "local", "return", "if", "then", "end", "for", "while"]
        if not any(kw in text.lower() for kw in lua_keywords):
            return None

        return text

    async def learn_module(self, module_name: str, test_fn, teacher_fn=None) -> bool:
        """Full learning cycle for one module (async)."""
        module = self.get_module(module_name)
        if not module:
            print(f"[Student] Module '{module_name}' not found")
            return False

        if module.get("status") == "confirmed":
            print(f"[Student] Module '{module_name}' already confirmed")
            return True

        max_attempts = 5
        for attempt in range(max_attempts):
            # Generate Lua code (async)
            lua_code = await self.generate_lua(module_name)
            if not lua_code:
                logger.warning(f"[Student] Failed to generate code for '{module_name}'")
                continue

            # Test the code
            if asyncio.iscoroutinefunction(test_fn):
                passed, error = await test_fn(lua_code)
            else:
                passed, error = await asyncio.to_thread(test_fn, lua_code)

            if passed:
                self.confirm_module(module_name, lua_code)
                return True
            else:
                logger.warning(f"[Student] Test failed: {error}")
                self.reject_module(module_name, error)

                # If too many failures, ask teacher
                if attempt >= 2 and teacher_fn:
                    logger.info(f"[Student] Asking teacher for help...")
                    teacher_fn(module_name, lua_code, error)

        logger.warning(f"[Student] Failed to learn '{module_name}' after {max_attempts} attempts")
        return False

    def get_confirmed_layers(self) -> list:
        """Get all confirmed modules sorted by level."""
        mods = self.modules.get("modules", {})
        confirmed = [
            (name, mod) for name, mod in mods.items()
            if mod.get("status") == "confirmed"
        ]
        return sorted(confirmed, key=lambda x: x[1].get("level", 999))

    def get_active_lua(self) -> str:
        """Get combined Lua code of all confirmed modules."""
        layers = self.get_confirmed_layers()
        parts = []
        for name, mod in layers:
            code = mod.get("lua_code", "")
            if code:
                parts.append(f"-- Layer {mod['level']}: {name}\n{code}")
        return "\n\n".join(parts)

    def memory_usage(self) -> dict:
        """Report memory usage of the student model."""
        mods = self.modules.get("modules", {})
        confirmed = sum(1 for m in mods.values() if m.get("status") == "confirmed")
        pending = sum(1 for m in mods.values() if m.get("status") == "pending")
        rejected = sum(1 for m in mods.values() if m.get("status") == "rejected")
        inactive = sum(1 for m in mods.values() if m.get("status") == "inactive")

        total_code_size = sum(
            len(m.get("lua_code", "") or "")
            for m in mods.values()
        )

        return {
            "total_modules": len(mods),
            "confirmed": confirmed,
            "pending": pending,
            "rejected": rejected,
            "inactive": inactive,
            "total_code_bytes": total_code_size,
            "ollama_model": _MODEL_CACHE.get("active", OLLAMA_MODEL_BASE),
            "ollama_model_size_mb": 522,
        }


