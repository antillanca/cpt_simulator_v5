def load_modules() -> dict:
    """Load module registry from JSON."""
    if MODULES_FILE.exists():
        return json.loads(MODULES_FILE.read_text())
    return {"modules": {}}


def save_modules(data: dict):
    """Persist module registry."""
    MODULES_FILE.write_text(json.dumps(data, indent=2))


_MODEL_CACHE = {"active": None, "last_check": 0}

async def get_active_model(subject: str = None) -> str:
    """Select the unified MoE model. The actual routing to experts (LoRA adapters) 
    happens internally within the model inference engine."""
    
    # We will use a unified base model that has the MoE architecture built-in
    # or a Multi-LoRA server that dynamically loads adapters.
    target_model = "qwen3-moe:latest"
    
    cache_key = target_model
    now = time.time()
    if _MODEL_CACHE.get(cache_key) and (now - _MODEL_CACHE.get(f"{cache_key}_time", 0) < 300):
        return _MODEL_CACHE[cache_key]

    import httpx
    try:
        async with httpx.AsyncClient(timeout=5) as client:
            resp = await client.get("http://localhost:11434/api/tags")
            if resp.status_code == 200:
                models = [m["name"] for m in resp.json().get("models", [])]
                if target_model in models or target_model.replace(":latest", "") in models:
                    _MODEL_CACHE[cache_key] = target_model
                    _MODEL_CACHE[f"{cache_key}_time"] = now
                    return target_model
    except Exception:
        pass

    # Fallback to the standard base model if MoE is not yet compiled
    return OLLAMA_MODEL_BASE


async def ollama_generate(prompt: str, model: str = None, timeout: int = 120) -> Optional[str]:
    """Call local Qwen3 via Ollama HTTP API (async)."""
    import httpx

    if not model:
        model = await get_active_model()
    payload = {
        "model": model,
        "prompt": prompt,
        "stream": False,
        "options": {"temperature": 0.3, "num_predict": 512, "think": False},
    }

    try:
        async with httpx.AsyncClient(timeout=timeout) as client:
            resp = await client.post(OLLAMA_URL, json=payload)
            body = resp.json()
            return body.get("response", "").strip()
    except Exception as e:
        logger.error(f"[Student] Ollama error: {e}")
        return None


class StudentEngine:
    """LLM student that learns by generating and testing Lua code.
    
    Learning flow:
    1. Student receives exercise (generated by teacher)
    2. Student generates Lua code for the concept
    3. Code is tested in sandbox
    4. If test passes → confirmed, module saved as layer
    5. If test fails → student retries with error feedback
    6. After N failures → teacher intervenes
    """

    def __init__(self):
        self.modules = load_modules()
        self.current_level = self._get_next_pending_level()
        self._last_model_used = OLLAMA_MODEL_BASE

    def _get_next_pending_level(self) -> int:
        """Find the next unconfirmed module level."""
        for name, mod in sorted(
            self.modules.get("modules", {}).items(),
            key=lambda x: x[1].get("level", 999),
        ):
            if mod.get("status") == "pending":
                return mod["level"]
        return 999

    def get_module(self, name: str) -> Optional[dict]:
        """Get a module by name."""
        return self.modules.get("modules", {}).get(name)

    def confirm_module(self, name: str, lua_code: str, teacher: str = "owl-alpha", generated_by: str = None):
        """Confirm a module — crystallize it as a permanent layer."""
        mods = self.modules.setdefault("modules", {})
        if name in mods:
            mods[name]["status"] = "confirmed"
            mods[name]["lua_code"] = lua_code
            mods[name]["confirmed_by"] = teacher
            mods[name]["generated_by"] = generated_by or self._last_model_used or OLLAMA_MODEL_BASE
            mods[name]["confirmed_at"] = time.strftime("%Y-%m-%dT%H:%M:%S")
            save_modules(self.modules)
            logger.info(f"[Student] ✅ Module '{name}' confirmed by {teacher}, generated_by={mods[name]['generated_by']}")
            return True
        return False

    def reject_module(self, name: str, reason: str):
        """Reject a module — mark for re-learning."""
        mods = self.modules.setdefault("modules", {})
        if name in mods:
            mods[name]["status"] = "rejected"
            mods[name]["rejection_reason"] = reason
            mods[name]["rejection_count"] = mods[name].get("rejection_count", 0) + 1
            save_modules(self.modules)
            logger.warning(f"[Student] ❌ Module '{name}' rejected: {reason}")

    def deactivate_module(self, name: str):
        """Deactivate a module — free memory, keep for reference."""
        mods = self.modules.setdefault("modules", {})
        if name in mods:
            mods[name]["status"] = "inactive"
            mods[name]["lua_code"] = None  # Free memory
            save_modules(self.modules)
            logger.info(f"[Student] 💤 Module '{name}' deactivated (memory freed)")

    def _get_cognitive_context(self, module: Optional[dict]) -> str:
        """Get the cognitive grounding context based on the current level."""
        if not module:
            return "You are an AI learning and exploring reality through code."
            
        level = module.get("level", 0)
        if level <= 1:
            return "Logical Transformation Layer: You are learning to distinguish differences and count. Use numbers to represent raw states."
        elif level <= 6:
            return "Mathematical Abstraction Layer: You are learning numerical operations and functions. Represent pure logic using math."
        elif level <= 9:
            return "Geometric Representation Layer: You are learning about space, vectors, and time. Represent spatial relationships (x, y, distance)."
        elif level <= 17:
            return "Physical Simulation Layer: You are learning physics and causality. Manipulate forces (vx, vy, acceleration) to affect the 'particle'."
        else:
            return "Advanced Abstraction Layer: You are learning complex systems and theoretical physics. Use advanced mathematics to model the frontier of reality."

